    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
         String sql="SELECT * FROM tasks";
        try{
            PreparedStatement pst= con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            DefaultTableModel model = (DefaultTableModel)tblManageTasks.getModel();
            while(rs.next())
                model.addRow(new String[]{rs.getString(1), rs.getString(2)});
                    }catch(SQLException ex){
                        System.out.println("Error : " + ex.getMessage());
        }
    }//GEN-LAST:event_btnViewActionPerformed

    private void dateKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_dateKeyReleased
        // TODO add your handling code here:
        Pattern patt=Pattern.compile("^[0-9]{1,31}[-]{0,1}[0-9]{1,31}[-]{0,1}[0-9]{1,31}$");
        Matcher match=patt.matcher(date.getText());
        if(!match.matches()){
           dlab.setText("Date is incorrect");
        }
        else{
          dlab.setText(null);
          
        }
    }//GEN-LAST:event_dateKeyReleased

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        

        if(date.getText().equals(""))
            JOptionPane.showMessageDialog(null, "Date is Mandatory!");
        else if(tasks.getText().equals(""))
            JOptionPane.showMessageDialog(null, "Task is Mandatory!");
        else 
           { 

        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con= DriverManager.getConnection("jdbc:mysql://localhost:3306/taskmanagement","root","Hellojavashit69");
            
            String sql=" insert into tasks values (?,?)";
            java.sql.PreparedStatement pst;
            pst = con.prepareStatement(sql);
            pst.setString(1, date.getText());
            pst.setString(2, tasks.getText());
                       
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Added Successfully");
            dispose();
            Manage_Tasks lg=new Manage_Tasks();
            lg.show();
            
            con.close();
            }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
         DefaultTableModel tblModel = (DefaultTableModel) tblManageTasks.getModel();
       
       
        //Delete row
        if(tblManageTasks.getSelectedRowCount()==1){
            
            tblModel.removeRow(tblManageTasks.getSelectedRow());
        }
        else
        {
            if(tblManageTasks.getRowCount() ==0)
            {
                JOptionPane.showMessageDialog(this, "Please select Single Row For Delete.");
            }else{
                JOptionPane.showMessageDialog(this, "Table is Empty."); 
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

